function barAnimated(width, time) {
  let barCounter = Math.ceil(window.innerWidth / width);
  let animationLayer = document.querySelector(".animationLayer2");

  let barsElement = Array(barCounter).fill(
    createBarElement(width, window.innerHeight)
  );

  animationLayer.style.width = "100vw";
  animationLayer.style.height = "100vh";
  animationLayer.style.display = "flex";
  animationLayer.classList.remove("show2");
  animationLayer.innerHTML = "";
  // document.querySelector("body").innerHTML = "";

  barsElement.map((item) => (animationLayer.innerHTML += item));
  // barsElement.map((item) => (document.querySelector("body").innerHTML += item));

  let bars = Array.from(document.querySelectorAll(".barAnimated"));
  let counter;
  function start() {
    if (!counter) counter = 0;
    if (counter < barCounter) {
      setTimeout(function () {
        bars[counter].classList.add("go");
        counter++;
        start(counter);
      }, time / barCounter);
    }
    if (counter == barCounter) {
      animationLayer.classList.add("show2");
      animationLayer.innerHTML = "ARENA!";
    }
  }

  start();
}
