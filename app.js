class Card {
  constructor(rarity, id, strength, speed, magic, element, name) {
    this.name = !!name ? name : this.getName();
    this.rarity = rarities.find((item) => item.abrv === rarity);
    if (!strength || !speed || !magic) {
      let _attributes = this.getRandomattributes();
      this.strength = _attributes.strength;
      this.speed = _attributes.speed;
      this.magic = _attributes.magic;
    } else {
      this.strength = strength;
      this.speed = speed;
      this.magic = magic;
    }
    this.totalattributePoints = this.strength + this.speed + this.magic;
    this.element = !!element ? element : this.getElement();
    this.speciality = this.getSpeciality();
    this.avatarImage = `imgs/cards-face/avatar${getRandomNumber(1, 12)}.png`;
    this.grade = this.getGrade();
    this.id = !id ? 0 : id;
  }

  getattributes() {
    return { strength: this.strength, speed: this.speed, magic: this.magic };
  }

  attributes() {
    return this.getattributes();
  }

  getSpeciality() {
    let object = this.attributes();
    let max = Math.max(...Object.values(object));
    let result = [];

    for (let prop in object) {
      if (object[prop] === max) result.push(prop);
    }
    let _speciality = result[getRandomNumber(0, result.length - 1)];
    return specialities.find((item) => item.name === _speciality);
  }

  getName() {
    name = `${names[getRandomNumber(0, names.length)]} ${
      surnames[getRandomNumber(0, surnames.length)]
    }`;
    return name;
  }

  getElement() {
    return elements[getRandomNumber(0, elements.length - 1)];
  }

  getRandomattributes() {
    let _rarity = this.rarity.abrv;

    let pointsToDistribute, max;

    if (_rarity == "r") pointsToDistribute = 26;
    if (_rarity == "u") pointsToDistribute = 23;
    if (_rarity == "c") pointsToDistribute = 20;

    let min = pointsToDistribute - 20 < 5 ? 5 : pointsToDistribute - 20;

    let first = getRandomNumber(min, 10);
    let second, third;

    pointsToDistribute -= first;
    min = pointsToDistribute - 10 < 5 ? 5 : pointsToDistribute - 10;
    max = pointsToDistribute - 5 > 10 ? 10 : pointsToDistribute - 5;

    second = getRandomNumber(min, max);

    pointsToDistribute -= second;

    third = pointsToDistribute;

    let finalPoints = [first, second, third];

    let randomIndex = getRandomNumber(0, finalPoints.length - 1);

    let str = finalPoints[randomIndex];

    finalPoints.splice(randomIndex, 1);

    randomIndex = getRandomNumber(0, finalPoints.length - 1);

    let spd = finalPoints[randomIndex];

    finalPoints.splice(randomIndex, 1);

    let mgc = finalPoints[0];

    return { strength: str, speed: spd, magic: mgc };
  }

  getIcons() {
    let strIcon = "imgs/cards-icons/attributeStr.svg";
    let spdIcon = "imgs/cards-icons/attributeSpd.svg";
    let mgcIcon = "imgs/cards-icons/attributeMgc.svg";
    return { strength: strIcon, speed: spdIcon, magic: mgcIcon };
  }

  create() {
    let cardMock = `
    <div class="card"
    style="background-image: url('imgs/cardFrame.png'), url(${
      this.avatarImage
    });">
    <div class="title">
      ${this.name}
    </div>
    <div class="spacer"></div>
    <div class="cardData">
      <div class="attributes">
        <div class="strength">
          <img src="${this.getIcons().strength}" alt="" />
          <span>${this.strength}</span>
        </div>
        <div class="speed">
          <img src="${this.getIcons().speed}" alt="" />
          <span>${this.speed}</span>
        </div>
        <div class="magic">
          <img src="${this.getIcons().magic}" alt="" />
          <span>${this.magic}</span>
        </div>
      </div>
      <div class="feature">
        <div class="specialty">
          <img src="${this.speciality.icon}" alt="" />
        </div>
        <div class="element">
          <img src="${this.element.icon}" alt="" />
        </div>
      </div>
    </div>
    <div class="rarity">
      <img src="${this.rarity.icon}" alt="" />
    </div>
    </div>  
    `;
    return cardMock;
  }

  getGrade() {
    function gradeValues(attr) {
      if (attr == 10) return 32;
      if (attr == 9) return 16;
      if (attr == 8) return 8;
      if (attr == 7) return 4;
      if (attr == 6) return 2;
      if (attr == 5) return 1;
    }
    let result = 0;
    for (let prop in this.attributes()) {
      result += gradeValues(this.attributes()[prop]);
    }
    return result;
  }
}

